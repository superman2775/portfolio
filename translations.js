function updateTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const parts = key.split('.');
    let translation = translations[lang];
    parts.forEach(part => {
      translation = translation ? translation[part] : '[MISSING]';
    });
    el.textContent = translation || el.textContent;
  });
  // Update title and description
  document.title = translations[lang]?.title || document.title;
  const metaDesc = document.querySelector('meta[name="description"]') || document.querySelector('#metaDescription');
  if (metaDesc) metaDesc.content = translations[lang]?.description || metaDesc.content;
}

function initI18n() {
  const savedLang = localStorage.getItem('lang') || navigator.language.split('-')[0];
  const lang = ['en', 'nl-BE', 'de-AT', 'fr-BE'].includes(savedLang) ? savedLang : 'en';
  document.documentElement.lang = lang;
  document.getElementById('langSelect')?.value = lang;
  updateTranslations(lang);
}

export { translations, updateTranslations, initI18n };

